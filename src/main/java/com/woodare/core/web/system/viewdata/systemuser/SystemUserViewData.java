package com.woodare.core.web.system.viewdata.systemuser;

import com.woodare.core.jpa.data.systemuser.SystemUserData;

/**
 * 
 * ClassName: UserViewData
 * 
 * @description
 * @author
 * @Date 2014/10/08
 * 
 */
public class SystemUserViewData extends SystemUserData {
	private static final long serialVersionUID = -8632449877318520090L;

	private String newPassword;

	private String newPasswordConfirm;

	private Boolean isSuperFlag;

	private Integer age;

	/**
	 * @return the isSuperFlag
	 */
	public Boolean getIsSuperFlag() {
		return isSuperFlag;
	}

	/**
	 * @param isSuperFlag
	 *            the isSuperFlag to set
	 */
	public void setIsSuperFlag(Boolean isSuperFlag) {
		this.isSuperFlag = isSuperFlag;
	}

	/**
	 * @return the newPassword
	 */
	public String getNewPassword() {
		return newPassword;
	}

	/**
	 * @param newPassword
	 *            the newPassword to set
	 */
	public void setNewPassword(String newPassword) {
		this.newPassword = newPassword;
	}

	/**
	 * @return the newPasswordConfirm
	 */
	public String getNewPasswordConfirm() {
		return newPasswordConfirm;
	}

	/**
	 * @param newPasswordConfirm
	 *            the newPasswordConfirm to set
	 */
	public void setNewPasswordConfirm(String newPasswordConfirm) {
		this.newPasswordConfirm = newPasswordConfirm;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

}
