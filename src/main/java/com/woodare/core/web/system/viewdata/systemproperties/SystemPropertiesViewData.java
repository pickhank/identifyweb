package com.woodare.core.web.system.viewdata.systemproperties;


import java.util.List;

import org.springframework.web.multipart.commons.CommonsMultipartFile;

import com.woodare.core.jpa.data.systemproperties.SystemPropertiesData;

/**
 * 
 * ClassName: SystemPropertiesViewData
 * 
 * @description
 * @author 
 * @Date 2014/10/08
 * 
 */
public class SystemPropertiesViewData extends SystemPropertiesData {
	private static final long serialVersionUID = -7467799779478963793L;
	/** 属性Codes */
    private List<String> pcodes;

    /** 属性值s */
    private List<String> pvalues;

    private CommonsMultipartFile apkFile;

    /**
     * @return the apkFile
     */
    public CommonsMultipartFile getApkFile() {
        return apkFile;
    }

    /**
     * @param apkFile
     *            the apkFile to set
     */
    public void setApkFile(CommonsMultipartFile apkFile) {
        this.apkFile = apkFile;
    }

    /**
     * @return the pcodes
     */
    public List<String> getPcodes() {
        return pcodes;
    }

    /**
     * @param pcodes
     *            the pcodes to set
     */
    public void setPcodes(List<String> pcodes) {
        this.pcodes = pcodes;
    }

    /**
     * @return the pvalues
     */
    public List<String> getPvalues() {
        return pvalues;
    }

    /**
     * @param pvalues
     *            the pvalues to set
     */
    public void setPvalues(List<String> pvalues) {
        this.pvalues = pvalues;
    }
}

